package com.currencyconverter.model;

public class Rates {
    private double ARS;
    private double BRL;
    private double COP;

    public double getArs() {
        return ARS;
    }

    public void setArs(double ars) {
        ARS = ars;
    }

    public double getBrl() {
        return BRL;
    }

    public void setBrl(double brl) {
        BRL = brl;
    }

    public double getCop() {
        return COP;
    }

    public void setCop(double cop) {
        COP = cop;
    }
}
